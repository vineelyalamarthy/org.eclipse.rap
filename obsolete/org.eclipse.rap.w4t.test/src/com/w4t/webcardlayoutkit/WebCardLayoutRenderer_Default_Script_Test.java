/** <p>Tests the rendering of org.eclipse.rap.WebCardLayout.</p>
  */
public class WebCardLayoutRenderer_Default_Script_Test
  extends WebCardLayoutRendererBase
{

  public WebCardLayoutRenderer_Default_Script_Test( final String name ) {
    super( name );
    setGenerateResources( false );
  }
  
  public void testRenderingKit() {
    RendererCache rendererCache = RendererCache.getInstance();
    Renderer renderer = rendererCache.retrieveRenderer( WebCardLayout.class, 
                                                        browser );
    assertEquals( WebCardLayoutRenderer_Default_Script.class, 
                  renderer.getClass() );
  }
  
  public void testRender() throws Exception {
    runRenderTest();
  }
  
  protected void setUp() throws Exception {
    super.setUp();
    offset = 0;
    browser = new Default( true );
  }
}
